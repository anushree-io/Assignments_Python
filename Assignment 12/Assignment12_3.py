# 3. Write a program which accepts two numbers and prints addition, subtraction,
# multiplication and division.

def main():
    No1 = int(input("Enter first number: "))
    No2 = int(input("Enter first number: "))

    add = No1+No2
    sub = No1-No2
    mult = No1*No2
    div = No1/No2

    print(add)
    print(sub)
    print(mult)
    print(div)

if __name__ == "__main__":
    main()



